			<h3 class="bold para-title">Lịch sử kỳ thi</h3>
				Vào đầu những năm 1980, Peter O&#39;Holloran, một thầy giáo dạy Toán người Úc, triển

khai một kỳ thi về Toán học giữa các trường tại Úc. Kỳ thi áp dụng một hình thức thi mới, sử

dụng các câu hỏi trắc nghiệm, chấm bài thi trên máy tính. Điều đó đã giúp cho hàng ngàn

học sinh trên toàn nước Úc có thể tham gia dự một cách nhanh chóng, hiệu quả. Kỳ thi này

(tiền thân của kỳ thi Toán quốc gia Úc – AMNC) đã đạt được thành công vang dội, nhận

được sự hưởng ứng nhiệt liệt của không chỉ các học sinh, giáo viên trên toàn nước Úc mà

cả các quốc gia khác trên thế giới.<br><br>

Từ ý tưởng này, hai giáo viên người Pháp André Deledicq và Jean Pierre Boudine đã

mang kỳ thi này về nước mình. Tên gọi của kỳ thi “Kangaroo” là để tri ân thầy giáo người Úc

– Peter O&#39;Holloran đã sáng tạo ra hình thức thi vô cùng hấp dẫn, hiệu quả. Lần đầu tiên tổ

chức tại Pháp vào năm 1991 đã có hơn 120.000 học sinh tham dự. Chỉ vài năm sau đó, kỳ

thi lan rộng ra hơn 20 quốc gia ở Châu Âu. Các quốc gia này sau đó đã cùng nhau thành lập

Association Kangourou sans Frontie (AKSF), tạm dịch là Hiệp hội Toán học Kangaroo không

biên giới để cùng nhau điều hành và phát triển kỳ thi.<br><br>

Trải qua gần 2 thập kỷ phát triển và lớn mạnh, cho đến nay, đây là kỳ thi Toán học có

số lượng thí sinh tham dự lớn nhất trên thế giới, thu hút trên 6.000.000 thí sinh đến từ

khoảng 70 quốc gia mỗi năm. Chủ tịch hiện tại của Hiệp hội Toán quốc tế Kangaroo là Tiến

sĩ Gregor Dolinar – Trường Đại học Ljubjana, Slovenia. Ông cũng là Tổng thư ký của Hội

đồng cố vấn Kỳ thi Olympic Toán học quốc tế.<br>
<h3 class="bold para-title">Kỳ thi IKMC tại Việt Nam</h3>
	Tháng 03/2016, lần đầu tiên Kỳ thi Toán Kangaroo – IKMC được tổ chức tại Việt Nam

do Trung tâm Phát triển tư duy và Kỹ năng IEG phối hợp với Đại học Giáo dục (ĐH Quốc

gia Hà Nội) tổ chức tại 5 điểm thi tập trung (4 điểm thi tại Hà Nội và 1 điểm thi tại TP Hồ Chí

Minh). Trong lần đầu tiên ra mắt, kỳ thi thu hút hơn 6000 thí sinh tới từ 327 trường thuộc 17

tỉnh thành trên cả nước, góp phần thúc đẩy hứng thú, niềm đam mê Toán học và thái độ học

tập tích cực của các em học sinh một cách toàn diện. Năm nay kỳ thi tiếp tục tổ chức với

quy mô rộng hơn, dự kiến các điểm thi IKMC 2017 sẽ có mặt tại TP Hà Nội, TP Hồ Chí Minh

và một vài Tỉnh thành khác.
